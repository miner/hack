(ns advent21.adv10
  (:require [clojure.string :as str]))

(def stchk
  {\] \[
   \} \{ 
   \) \( 
   \> \<})


(defn chunk-error [s]
  (let [errch (reduce (fn [stack ch]
            (if-let [start (stchk ch)]
              (if (= (peek stack) start)
                (pop stack)
                (reduced ch))
              (conj stack ch)))
          []
          s)]
    (case errch
      \) 3
      \] 57
      \} 1197
      \> 25137
      0)))

;; part 1
(defn chunk-score [input]
  (reduce + (map chunk-error (str/split-lines input))))


(def revchk
  {\[ \]
   \{ \} 
   \( \) 
   \< \>})

;; part2
(defn completion [s]
  (let [remaining (reduce (fn [stack ch]
            (if-let [start (stchk ch)]
              (if (= (peek stack) start)
                (pop stack)
                (reduced false))
              (conj stack ch)))
          []
          s)
        com (reduce (fn [cs c] (conj cs (revchk c)))
                       nil
                       remaining)]
    (reduce (fn [score c]
              (+ (* 5 score)
                 (case c
                   \) 1
                   \] 2
                   \} 3
                   \> 4)))
            0
            com)))



;; only return the middle score
(defn score-completions [input]
  (let [incompletes (filter #(zero? (chunk-error %)) (str/split-lines input))
        mid (quot (count incompletes) 2)]
    (nth (sort (map completion incompletes)) mid)))







